# Time in state
def time_in_state(group):
    result = []
    last_time = group['timestamp'].iloc[0]
    last_state = group['state'].iloc[0]

    for _, row in group.iterrows():
        if row['state'] != last_state:
            last_time = row['timestamp']
            last_state = row['state']
        result.append((row['timestamp'] - last_time).total_seconds())

    return pd.Series(result, index=group.index)

df['time_in_state_sec'] = df.groupby('node_name', group_keys=False).apply(time_in_state)

