import numpy as np

for node in df['node_name'].unique():
    node_idx = df[df['node_name'] == node].index

    failure_starts = np.random.choice(
        node_idx[:-5],
        size=max(1, len(node_idx)//40),
        replace=False
    )

    for start in failure_starts:

        seq_idx = range(start, min(start + 6, node_idx[-1] + 1))

        for i, idx in enumerate(seq_idx):

            # Early instability
            if i == 0:
                df.loc[idx, 'latency'] += np.random.randint(20, 40)

            # Rising instability
            elif i == 1:
                df.loc[idx, 'latency'] += np.random.randint(40, 80)

            # Packet-loss beginning
            elif i == 2:
                df.loc[idx, 'latency'] += np.random.randint(80, 140)
                df.loc[idx, 'fails'] += 1

            # Degraded state
            elif i == 3:
                df.loc[idx, 'state'] = 'DEGRADED'
                df.loc[idx, 'latency'] += np.random.randint(150, 250)

            # Severe degradation
            elif i == 4:
                df.loc[idx, 'state'] = 'DEGRADED'
                df.loc[idx, 'latency'] += np.random.randint(250, 400)
                df.loc[idx, 'fails'] += 2

            # Actual failure
            elif i >= 5:
                df.loc[idx, 'state'] = 'DOWN'
                df.loc[idx, 'latency'] = -1
                df.loc[idx, 'fails'] += 3
                df.loc[idx, 'ping_failed'] = 1


