# Cell 4 — Feature Engineering Creating predictive features per node using rolling windows

print(' Engineering features...\\n')
# Group by node for rolling calculations----------------------------------------------------
df = df.sort_values(['node_name', 'timestamp']).reset_index(drop=True)

# Rolling window size (last 5 readings per node)
WINDOW = 5

# Feature 1: Rolling Average Latency (last 5 readings)--------------------------------------
df['rolling_avg_latency'] = df.groupby('node_name')['latency'].transform(lambda x: x.rolling(window=WINDOW, min_periods=1).mean())
# Feature 2: Rolling Max Latency (last 5 readings)------------------------------------------
df['rolling_max_latency'] =   df.groupby('node_name')['latency'].transform(lambda x: x.rolling(window=WINDOW, min_periods=1).max())
# Feature 3: Rolling Std Latency (last 5 readings)-----------------------------------------# High std = unstable connection
df['rolling_std_latency'] =  df.groupby('node_name')['latency'].transform(lambda x: x.rolling(window=WINDOW, min_periods=1).std().fillna(0))
# Feature 4: Latency Trend (current - previous)------------------------------------# Positive = getting worse, Negative = improving
df['latency_trend'] = df.groupby('node_name')['latency'].transform(lambda x: x.diff().fillna(0))
#  Feature 5: Fail Count (already in data)-------------------------------------# Already exists as 'fails' column
# Feature 6: Rolling Fail Sum (last 5 readings)
df['rolling_fail_sum'] = df.groupby('node_name')['fails'] .transform(lambda x: x.rolling(window=WINDOW, min_periods=1).sum())
# Feature 7: Previous State-------------------------------------------
df['prev_state_encoded'] = df.groupby('node_name')['state_encoded'].transform(lambda x: x.shift(1).fillna(0))
# Feature 8: Time in Current State (seconds)-------------------------
def time_in_state(group):
    result = []
    last_change_time = group['timestamp'].iloc[0]
    last_state = group['state'].iloc[0]

    for _, row in group.iterrows():
        if row['state'] != last_state:
            seconds = (row['timestamp'] - last_change_time).total_seconds()
            result.append(seconds)

            last_change_time = row['timestamp']
            last_state = row['state']
        else:
            result.append(0)

    return pd.Series(result, index=group.index)


# Apply function
df['time_in_state_sec'] = df.groupby('node_name', group_keys=False).apply(time_in_state)

