# Cell 5 — Label Creation. will_fail_soon = 1` if node goes DOWN within next 3 log entries
print(' Creating labels...\\n')
# Look ahead N steps to see if node goes DOWN
LOOKAHEAD = 3

def will_fail_soon(group):
    """For each row, check if the node goes DOWN within the next LOOKAHEAD rows (per node).
    Returns: 1 = failure coming, 0 = safe
    """
    states = group['state'].values
    labels = []

    for i in range(len(states)):
        future = states[i+1 : i+1+LOOKAHEAD]

        if 'DOWN' in future:
            labels.append(1)  # failure coming soon
        else:
            labels.append(0)  # safe

    return pd.Series(labels, index=group.index)

df['will_fail_soon'] = df.groupby('node_name', group_keys=False).apply(will_fail_soon)

